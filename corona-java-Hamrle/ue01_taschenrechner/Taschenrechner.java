package ue01_taschenrechner;

public class Taschenrechner
{
	
	public static double a;
	public static double b;
	
	public static double getAdditionResult(double a, double b)
	{
		return a + b;
	}
	
	public static double getSubtractionResult(double a, double b)
	{
		return a - b;
	}

	public static double getMultiplicationResult(double a, double b)
	{
		return a * b;
	}

	public static double getDivisionResult(double a, double b)
	{
		return a / b;
	}
}
